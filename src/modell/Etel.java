package modell;

public class Etel {

    private String etelnev;
    private int etelAr;

    public Etel(String etelnev, int etelAr) {
        this.etelnev = etelnev;
        this.etelAr = etelAr;
    }

    public String getEtelnev() {
        return etelnev;
    }

    public int getEtelAr() {
        return etelAr;
    }   
}
